class ArithmaticOperations
{
    public static void Main()
    {
        int num1 = 100;
        int num2 = 10;

        System.Console.WriteLine("Addition :"+(num1 + num2));
        System.Console.WriteLine("Subtraction :"+(num1 - num2));
        System.Console.WriteLine("Multipiation :"+(num1 * num2));
        System.Console.WriteLine("Division :"+(num1/num2));
        System.Console.WriteLine("Mod :"+(num1 % num2));
    }
}